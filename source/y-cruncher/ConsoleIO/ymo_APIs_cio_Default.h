//  Basic Printing
void ymo_print_astr(const char *str){
    while (*str != '\0'){
        wprintf(L"%c",(wchar_t)*str);
        str++;
    }
    fflush(stdout);
}
void ymo_print_wstr(const wchar_t *str){
    //  Global print function with Unicode support.

    while (*str != L'\0'){
        wprintf(L"%c",(wchar_t)*str);
        str++;
    }
    fflush(stdout);
}
void ymo_scan_str(wchar_t *buffer,ym_pL L){
    if (L == 0)
        return;
    ymo_set_color_T(1);
    ym_pL c = 0;
    while (c < L - 1){
        *buffer = fgetwc(stdin);
        if (buffer[0] < 32){
            if (c == 0)
                continue;
            break;
        }
        
        buffer++;
        c++;
    }
    ymo_set_color_W();
    *buffer = L'\0';
}
