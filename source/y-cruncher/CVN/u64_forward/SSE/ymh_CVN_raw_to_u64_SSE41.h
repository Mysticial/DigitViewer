//  Dependencies
#include "../../ymb_CVN_headers.h"
#include "../Default/ymi_CVN_rawh_to_ub_Default.h"
#include "../Default/ymi_CVN_rawd_to_ud_Default.h"
#include "ymi_CVN_rawh_to_ub_SSE2.h"
#include "ymi_CVN_rawd_to_ud_SSE41.h"
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void ymb_CVN_rawh_to_u64b_f(ym_u64 *T,const char *A,ym_pL TL){
    const ym_u64 *stopT = T + TL;

    while (T < stopT - 3){
        ymi_CVN_rawh_to_u64b_u2_SSE2(T,A);
        A += 64;
        T += 4;
    }
    while (T < stopT - 1){
        ymi_CVN_rawh_to_u64b_u1_SSE2(T,A);
        A += 32;
        T += 2;
    }
    if (T < stopT){
        ymi_CVN_rawh_to_u64b_u0_Default(T,A);
        A += 16;
        T += 1;
    }
}
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
void ymb_CVN_rawd_to_u64d_f(ym_u64 *T,const char *A,ym_pL TL){
    const ym_u64 *stopT = T + TL;
    
    while (T < stopT - 3){
        ymi_CVN_rawd_to_u64d19_u2_SSE41(T,A);
        A += 76;
        T += 4;
    }
    if (T < stopT - 1){
        ymi_CVN_rawd_to_u64d19_u1_SSE41(T,A);
        A += 38;
        T += 2;
    }
    if (T < stopT){
        ymi_CVN_rawd_to_u64d19_u0_Default(T,A);
        A += 19;
        T += 1;
    }
}
