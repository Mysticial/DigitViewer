int ymb_CVN_strd_to_rawd_f(char *T,ym_pL L){
    char *stop = T + L;

    ym_u32 bad = 0;
    while (T < stop - 15){
        ymi_CVN_strd_to_rawd_u4_Default32(T,bad);
        T += 16;
    }
    while (T < stop - 7){
        ymi_CVN_strd_to_rawd_u3_Default32(T,bad);
        T += 8;
    }
    while (T < stop - 3){
        ymi_CVN_strd_to_rawd_u2_Default32(T,bad);
        T += 4;
    }
    while (T < stop){
        ymi_CVN_strd_to_rawd_u0_Default(T,bad);
        T += 1;
    }

    return bad == 0 ? 0 : 1;
}
