#pragma once
#ifndef ymp_Exceptions_StdException_H
#define ymp_Exceptions_StdException_H
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  Dependencies
#include <string>
#include "Exception.h"
namespace ymp{
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
class StdException : public Exception{
public:
    static const char TYPENAME[];

    YM_NO_INLINE StdException();
    YM_NO_INLINE StdException(const char* message);
    YM_NO_INLINE StdException(std::string message);

public:
    [[noreturn]] virtual void fire() const override{
        throw *this;
    }
    virtual const char* get_typename() const override{
        return TYPENAME;
    }
    virtual Exception* clone() const override{
        return new StdException(*this);
    }
    virtual void print() const override;

    const std::string& message() const{
        return m_message;
    }

public:
    StdException(const DllSafeStream& data);
    virtual DllSafeStream serialize() const override;

protected:
    std::string m_message;
};
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
}
#endif
