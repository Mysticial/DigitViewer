#pragma once
#ifndef _ymp_Types_H
#define _ymp_Types_H
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
#include <stdint.h>
#include <limits.h>
#include <stddef.h>
namespace ymp{
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  Fixed Sizes
typedef uint16_t    u16_t;
typedef uint32_t    u32_t;
typedef int32_t     s32_t;
typedef uint64_t    u64_t;
typedef int64_t     s64_t;
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  Logarithmic Size
typedef uint_fast32_t   ukL_t;
typedef int_fast32_t    skL_t;
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  Pointer Sizes
typedef size_t      upL_t;
typedef ptrdiff_t   spL_t;
const ukL_t PTR_BITS = CHAR_BIT * sizeof(upL_t);
////////////////////////////////////////////////////////////////////////////////
#if _M_X64 || __x86_64
#define YM_PTR_MAG  6
#else
#define YM_PTR_MAG  5
#endif
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  File Sizes
typedef u64_t       ufL_t;
typedef s64_t       sfL_t;
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  Integer Index Size
typedef u64_t uiL_t;
typedef s64_t siL_t;
const ukL_t INDEX_BITS = CHAR_BIT * sizeof(uiL_t);
////////////////////////////////////////////////////////////////////////////////
#define YM_INDEX_MAG        6
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
//  Float Index Size
typedef double fiL_t;
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
}
#endif
